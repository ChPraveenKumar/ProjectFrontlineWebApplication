package com.frontline.webapi.output;

public class Domain {
	 private int id;
     private String name;
     private String tag;

     // Getters and Setters

     public int getId() {
         return id;
     }

     public void setId(int id) {
         this.id = id;
     }

     public String getName() {
         return name;
     }

     public void setName(String name) {
         this.name = name;
     }

     public String getTag() {
         return tag;
     }

     public void setTag(String tag) {
         this.tag = tag;
     }

}
